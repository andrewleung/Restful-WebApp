$(document).ready(function() {
	var forms = new Object();
	forms['consumerForm'] = $('#form_container');
	forms['dealerForm'] = $('#dealer_login_form');
	forms['login_select'] = $('#login_select');

	$('#consumerForm').click(function() {
		openForm('consumerForm');
	});
	
	$('#dealerForm').click(function() {
		openForm('dealerForm');
	});
	
	function openForm(keyName) {
		$.each(forms, function(key, value) {
			if (key == keyName) {
				forms[key].show();
			} else {
				forms[key].hide();
			}
		});
	}	
});

